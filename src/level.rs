pub fn get_level(rows: u32) -> u32 {
    rows / 10
}

pub fn get_speed_by_level(level: u32) -> u64 {
    match level {
        0 => 800,
        1 => 720,
        2 => 630,
        3 => 550,
        4 => 470,
        5 => 380,
        6 => 300,
        7 => 220,
        8 => 130,
        9 => 100,
        10 | 11 | 12 => 80,
        13 | 14 | 15 => 70,
        16 | 17 | 18 => 50,
        19..=28 => 30,
        _ => 17,
    }
}
